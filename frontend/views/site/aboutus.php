<h3>•   تستطيع تسجيل أجور ومرتبات العاملين بالكافي .</h3>
<h3>•   تستطيع تسجيل وصف للمصروفات لتسهيل التسجيل وعدم كتابتها في كل مرة ولكن تظهر لكي لتختار منها .</h3>
<h3>•   تستطيع استخراج تقارير عن المبيعات لكل صنف بحيث تقدر تعرف بيعت من كل صنف كمية قد ايه .</h3>
<h3>•   تستطيع إستخراج تقارير بمكسب الكافي .</h3>
<h3>•   تستطيع إستخراج تقارير يومية او اسبوعيه او شهويه أو سنوية أو أي فترة تريد الإستعلام عنها .</h3>
<h3>•   تستطيع إستخراج تقارير عن مصروفات الكافي وعن المرتبات الخاصة بالعاملين .</h3>
<h3>•   لا يستطيع اي شخص الحصول علي هذه التقاير غير مدير او صاحب الكافي .</h3>
<h3>•   لا يستطيع اي شخص تسجيل اي طلب أو الدخول علي السيستم بدون إذن صاحب الكافي .</h3>
<h3>•   تستطيع الحصول علي هذه الخدمات لفترة مجاناً .</h3>
<h3>•   تستطيع الدخول علي السيستم ومتابعة مطعمك من أي مكان عن طريق شراء هوست لموقعك علي النت .</h3>
<h3>•   تستطيع إذا حصلت علي هوست علي النت لموقعك انك تربط فروع مطعمك ببعض ويشتغلوا علي نفس السيستم .</h3>

   
  
